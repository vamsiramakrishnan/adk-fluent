def generate_deprecated_methods(specs: list[BuilderSpec]) -> str:
    """Extract deprecated method mappings from all BuilderSpecs."""
    lines = [HEADER.strip(), "", "# Deprecated Methods Reference", ""]
    lines.append("Methods that still work but should be replaced with their modern equivalents.")
    lines.append("These are defined in `seeds/seed.manual.toml` under `[builders.*.deprecated_aliases]`.")
    lines.append("")

    all_deprecated: dict[str, dict[str, str]] = {}
    for spec in specs:
        if spec.deprecated_aliases:
            for dep_name, dep_info in spec.deprecated_aliases.items():
                use = dep_info.get("use", "?")
                field = dep_info.get("field", "?")
                all_deprecated[dep_name] = {"use": use, "field": field, "builder": spec.name}

    if not all_deprecated:
        lines.append("No deprecated methods found.")
        return "\n".join(lines)

    lines.append("| Deprecated method | Use instead | Underlying field | Builder |")
    lines.append("|---|---|---|---|")
    for dep_name in sorted(all_deprecated):
        info = all_deprecated[dep_name]
        lines.append(f"| `.{dep_name}()` | `.{info['use']}()` | `{info['field']}` | {info['builder']} |")

    lines.append("")
    lines.append("## Quick grep patterns")
    lines.append("")
    lines.append("To find deprecated usage in code, search for these patterns:")
    lines.append("```")
    for dep_name in sorted(all_deprecated):
        lines.append(f".{dep_name}(")
    lines.append("```")

    return "\n".join(lines)


