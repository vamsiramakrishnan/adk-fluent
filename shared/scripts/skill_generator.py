def generate_builder_inventory(specs: list[BuilderSpec]) -> str:
    """Group builders by module with counts."""
    lines = [HEADER.strip(), "", "# Builder Inventory", ""]
    lines.append(f"**{len(specs)} builders** across the following modules:")
    lines.append("")

    groups: dict[str, list[BuilderSpec]] = defaultdict(list)
    for spec in specs:
        groups[spec.output_module].append(spec)

    for module in sorted(groups):
        builders = groups[module]
        lines.append(f"## {module} module ({len(builders)} builders)")
        lines.append("")
        for spec in sorted(builders, key=lambda s: s.name):
            extra_count = len(spec.extras)
            field_count = len([f for f in spec.fields if f["name"] not in spec.skip_fields])
            lines.append(f"- **{spec.name}** — `{spec.source_class}` ({field_count} fields, {extra_count} extras)")
        lines.append("")

    return "\n".join(lines)


