"""Emit one markdown page per component to ``docs/flux/components/``.

Each page ships:

  * A title (the catalog ``name``) and the LLM-facing description.
  * A props table derived from ``jsonSchema.properties`` (type,
    default, required, description).
  * Worked examples from ``llm.examples``.
  * Known anti-patterns (``llm.antiPatterns``).
  * The ``tokens`` array (what the renderer may reference).
  * Fallback info (which basic-catalog component picks up the slack when
    a renderer doesn't speak flux).

These pages are consumed by Sphinx via the ``myst_parser`` include and
are the canonical reference users land on from ``T.search(catalog="flux")``.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any

MARKER = "<!-- GENERATED BY shared/scripts/flux — DO NOT EDIT -->"


def _camel_to_kebab(name: str) -> str:
    """``FluxButton`` → ``flux-button``; ``FluxTextField`` → ``flux-text-field``."""
    s1 = re.sub(r"(.)([A-Z][a-z]+)", r"\1-\2", name)
    s2 = re.sub(r"([a-z0-9])([A-Z])", r"\1-\2", s1)
    return s2.lower()


def _prop_type_label(prop: dict[str, Any]) -> str:
    if "const" in prop:
        return f"`{prop['const']!r}`"
    if "enum" in prop:
        return "`" + " \\| ".join(repr(v) for v in prop["enum"]) + "`"
    t = prop.get("type")
    return f"`{t}`" if isinstance(t, str) else "`any`"


def _escape_cell(text: str) -> str:
    return text.replace("|", "\\|").replace("\n", " ")


def _render_page(spec: dict[str, Any]) -> str:
    name = spec["name"]
    extends = spec.get("extends") or ""
    description = (spec.get("llm") or {}).get("description") or ""
    js = spec.get("jsonSchema") or {}
    props: dict[str, Any] = js.get("properties") or {}
    required: list[str] = list(js.get("required") or [])

    lines: list[str] = [MARKER, "", f"# {name}", ""]
    if description:
        lines.append(description.strip())
        lines.append("")
    lines.append(f"**Extends:** `{extends}` (a2ui/basic@0.10)")
    lines.append("")
    lines.append(f"**Category:** `{spec.get('category', 'primitive')}`")
    lines.append("")

    # --- Props table
    if props:
        lines.append("## Props")
        lines.append("")
        lines.append("| Name | Type | Required | Default |")
        lines.append("| --- | --- | --- | --- |")
        for key in sorted(props):
            prop = props[key]
            type_label = _prop_type_label(prop)
            required_label = "yes" if key in required else "no"
            default_label = repr(prop["default"]) if "default" in prop else ""
            lines.append(f"| `{_escape_cell(key)}` | {type_label} | {required_label} | `{default_label}` |")
        lines.append("")

    # --- Examples
    examples = (spec.get("llm") or {}).get("examples") or []
    if examples:
        lines.append("## Examples")
        lines.append("")
        for ex in examples:
            caption = ex.get("caption", "")
            code = ex.get("code", "").strip()
            if caption:
                lines.append(f"_{caption}_")
                lines.append("")
            lines.append("```python")
            lines.append(code)
            lines.append("```")
            lines.append("")

    # --- Anti-patterns
    anti = (spec.get("llm") or {}).get("antiPatterns") or []
    if anti:
        lines.append("## Anti-patterns")
        lines.append("")
        for ap in anti:
            reason = ap.get("reason", "")
            code = ap.get("code", "").strip()
            lines.append(f"**Avoid:** {reason}")
            lines.append("")
            lines.append("```python")
            lines.append(code)
            lines.append("```")
            lines.append("")

    # --- Tokens
    tokens = spec.get("tokens") or []
    if tokens:
        lines.append("## Tokens used")
        lines.append("")
        for tok in sorted(tokens):
            lines.append(f"- `${tok}`")
        lines.append("")

    # --- Fallback
    renderer = spec.get("renderer") or {}
    fallback = renderer.get("fallback") or {}
    fb_component = fallback.get("component")
    fb_notes = fallback.get("notes")
    fb_map = fallback.get("map") or {}
    if fb_component:
        lines.append("## Basic-catalog fallback")
        lines.append("")
        lines.append(f"- **Component:** `{fb_component}`")
        if fb_map:
            mapping = ", ".join(f"`{k}` → `{v}`" for k, v in sorted(fb_map.items()))
            lines.append(f"- **Prop map:** {mapping}")
        if fb_notes:
            lines.append(f"- **Notes:** {fb_notes}")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def emit(catalog: dict[str, Any], *, root: Path) -> list[Path]:
    out_dir = root / "docs" / "flux" / "components"
    out_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    components = catalog.get("components") or {}
    sorted_names = sorted(components)
    slugs: list[tuple[str, str]] = []
    for name in sorted_names:
        spec = components[name]
        slug = _camel_to_kebab(name)
        page_path = out_dir / f"{slug}.md"
        page_path.write_text(_render_page(spec), encoding="utf-8")
        written.append(page_path)
        slugs.append((name, slug))

    # Emit a flux landing page that parents the component pages in the
    # Sphinx toctree. Without this, ``-W`` turns the ``toc.not_included``
    # warnings into hard errors.
    index_path = root / "docs" / "flux" / "index.md"
    index_path.write_text(_render_index(slugs), encoding="utf-8")
    written.append(index_path)
    return written


def _render_index(slugs: list[tuple[str, str]]) -> str:
    lines: list[str] = [
        MARKER,
        "",
        "# Flux catalog",
        "",
        "The **flux** catalog is the reference A2UI bundle shipped with",
        "adk-fluent. It extends `a2ui/basic@0.10` with ten Phase-1",
        "components designed for WCAG 2.2 AA, token-driven theming, and a",
        "basic-catalog fallback so renderers that don't speak flux still",
        "degrade gracefully.",
        "",
        "See `catalog/flux/ARCHITECTURE.md` for the layered contracts",
        "(tokens → specs → codegen → fluent API → React renderer).",
        "",
        "## Components",
        "",
        "```{toctree}",
        "---",
        "maxdepth: 1",
        "---",
    ]
    for _name, slug in slugs:
        lines.append(f"components/{slug}")
    lines.append("```")
    lines.append("")
    return "\n".join(lines)
