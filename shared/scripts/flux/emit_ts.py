"""Emit ``ts/src/flux/index.ts`` — TS factories per component.

Shape mirrors ``emit_py.py`` so Python and TS stay prop-compatible. Each
component gets:

  * A ``<Name>Node`` TypeScript type describing the factory's return shape.
  * A ``<name>`` (camelCase) factory function with the same required /
    optional split as the Python side.

The file is ESLint / prettier friendly: 2-space indent, no trailing
whitespace, single blank line between factories, LF line endings.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

MARKER = "// GENERATED BY shared/scripts/flux — DO NOT EDIT"

_HIDDEN_PROPS = frozenset({"component"})


def _camel_to_lower(name: str) -> str:
    """``FluxButton`` → ``fluxButton``."""
    if not name:
        return name
    return name[0].lower() + name[1:]


def _ts_type(prop: dict[str, Any]) -> str:
    if "const" in prop:
        val = prop["const"]
        if isinstance(val, str):
            return json.dumps(val)
        return "unknown"
    if "enum" in prop and isinstance(prop["enum"], list) and prop["enum"]:
        parts: list[str] = []
        for v in prop["enum"]:
            if isinstance(v, str):
                parts.append(json.dumps(v))
            elif isinstance(v, bool):
                parts.append("true" if v else "false")
            elif isinstance(v, (int, float)):
                parts.append(str(v))
            else:
                return "unknown"
        return " | ".join(parts)
    t = prop.get("type")
    if t == "string":
        return "string"
    if t == "integer" or t == "number":
        return "number"
    if t == "boolean":
        return "boolean"
    if t == "object":
        return "Record<string, unknown>"
    if t == "array":
        return "unknown[]"
    return "unknown"


def _ts_literal(value: Any) -> str:
    if value is None:
        return "undefined"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)):
        return json.dumps(value)
    if isinstance(value, str):
        return json.dumps(value)
    if isinstance(value, (list, tuple)):
        return "[" + ", ".join(_ts_literal(v) for v in value) + "]"
    if isinstance(value, dict):
        parts = [f"{json.dumps(k)}: {_ts_literal(v)}" for k, v in sorted(value.items())]
        return "{ " + ", ".join(parts) + " }"
    return "undefined"


def _render_factory(spec: dict[str, Any]) -> str:
    name = spec["name"]
    fn_name = _camel_to_lower(name)
    js = spec.get("jsonSchema") or {}
    props: dict[str, Any] = js.get("properties") or {}
    required: list[str] = list(js.get("required") or [])

    req_fields: list[tuple[str, str]] = []
    opt_fields: list[tuple[str, str, str]] = []
    required_seen: set[str] = set()
    for key in required:
        if key in _HIDDEN_PROPS:
            continue
        if key not in props:
            continue
        ann = _ts_type(props[key])
        req_fields.append((key, ann))
        required_seen.add(key)
    for key in sorted(k for k in props if k not in required_seen and k not in _HIDDEN_PROPS):
        prop = props[key]
        ann = _ts_type(prop)
        default_src = _ts_literal(prop["default"]) if "default" in prop else "undefined"
        opt_fields.append((key, ann, default_src))

    lines: list[str] = []
    description = (spec.get("llm") or {}).get("description") or f"{name} component factory."
    summary = description.strip().splitlines()[0][:120]

    # --- interface
    lines.append(f"/** {summary} */")
    lines.append(f"export interface {name}Node {{")
    lines.append(f'  component: "{name}";')
    for key, ann in req_fields:
        lines.append(f"  {key}: {ann};")
    for key, ann, _ in opt_fields:
        lines.append(f"  {key}?: {ann};")
    lines.append("}")
    lines.append("")

    # --- args type: required fields inline, optionals optional
    lines.append(f"/** Args accepted by {fn_name}() — maps 1:1 to {name}Node. */")
    lines.append(f"export interface {name}Args {{")
    for key, ann in req_fields:
        lines.append(f"  {key}: {ann};")
    for key, ann, _ in opt_fields:
        lines.append(f"  {key}?: {ann};")
    lines.append("}")
    lines.append("")

    # --- factory
    lines.append(f"/** Build a {name} node. */")
    lines.append(f"export function {fn_name}(args: {name}Args): {name}Node {{")
    lines.append(f"  const node: {name}Node = {{")
    lines.append(f'    component: "{name}",')
    for key, _ann in req_fields:
        lines.append(f"    {key}: args.{key},")
    lines.append("  };")
    for key, _ann, default_src in opt_fields:
        lines.append(f"  if (args.{key} !== undefined) {{")
        lines.append(f"    node.{key} = args.{key};")
        if default_src != "undefined":
            lines.append(f"  }} else if (node.{key} === undefined) {{")
            lines.append(f"    node.{key} = {default_src};")
        lines.append("  }")
    lines.append("  return node;")
    lines.append("}")
    return "\n".join(lines)


def _module_header() -> str:
    return (
        f"{MARKER}\n"
        "//\n"
        "// Typed factories for every flux component. Import directly:\n"
        "//\n"
        '//     import { fluxButton } from "../flux";\n'
        "//\n"
        "// or use the TS UI namespace shim (wired in W4).\n"
        "\n"
        'export const FLUX_CATALOG_ID = "flux/components@1";\n'
        'export const FLUX_CATALOG_VERSION = "1.0.0";\n'
        "\n"
    )


def emit(
    catalog: dict[str, Any],
    *,
    root: Path,
) -> Path:
    out_path = root / "ts" / "src" / "flux" / "index.ts"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    parts: list[str] = [_module_header()]
    components = catalog.get("components") or {}
    for name in sorted(components):
        parts.append(_render_factory(components[name]))
        parts.append("")
    # Re-export the renderer module so ``import { ... } from "./flux"`` also
    # exposes the React wiring once W5 fills in the scaffolds.
    parts.append('export * from "./renderer/types.js";')
    parts.append("")
    source = "\n".join(part.rstrip("\n") for part in parts).rstrip() + "\n"
    out_path.write_text(source, encoding="utf-8")
    return out_path
